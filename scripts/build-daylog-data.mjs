import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const rootDir = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const sourcePath = resolve(rootDir, 'data-source/daylog/entries.json');
const outputPath = resolve(rootDir, 'daylog_data.js');

function normalizeEntry(entry) {
  return {
    date: String(entry.date || ''),
    notes: Array.isArray(entry.notes) ? entry.notes.map((note) => String(note)) : [],
    anki: entry.anki && typeof entry.anki === 'object'
      ? {
          reviewCount: Number(entry.anki.reviewCount || 0),
          distinctCards: Number(entry.anki.distinctCards || 0),
          minutes: Number(entry.anki.minutes || 0),
        }
      : null,
  };
}

export function buildDayLogData() {
  const raw = readFileSync(sourcePath, 'utf8');
  const entries = JSON.parse(raw)
    .map(normalizeEntry)
    .sort((left, right) => right.date.localeCompare(left.date));

  mkdirSync(dirname(outputPath), { recursive: true });
  writeFileSync(
    outputPath,
    `// Generated by scripts/build-daylog-data.mjs\nconst dayLogEntries = ${JSON.stringify(entries, null, 2)};\n`,
    'utf8'
  );

  return entries;
}

const isDirectRun = process.argv[1] && import.meta.url === new URL(`file://${process.argv[1]}`).href;
if (isDirectRun) {
  buildDayLogData();
}
